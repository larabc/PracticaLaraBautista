package com.example.roompeliculas.model;

public class PeliculaAPI {

String id;
String title;

    public PeliculaAPI(String title) {
        this.title = title;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
